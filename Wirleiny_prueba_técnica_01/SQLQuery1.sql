Create Database Empresa;

CREATE TABLE Empleados (
    ID INT PRIMARY KEY IDENTITY(1,1), 
    Nombre NVARCHAR(100) NOT NULL,   
    Cargo NVARCHAR(50) NOT NULL,      
    SalarioBase DECIMAL(10,2) NOT NULL, 
    FechaIngreso DATE NOT NULL      
);

Select * From Empleados

CREATE TABLE HorasTrabajadas (
    ID INT PRIMARY KEY IDENTITY(1,1),  
    EmpleadoID INT NOT NULL,           
    Periodo DATE NOT NULL,            
    HorasNormales INT NOT NULL,        
    HorasExtras INT NOT NULL,         
    FOREIGN KEY (EmpleadoID) REFERENCES Empleados(ID) 
);

Select * From HorasTrabajadas 

CREATE TABLE Deducciones (
    ID INT PRIMARY KEY IDENTITY(1,1), 
    Tipo NVARCHAR(50) NOT NULL,        
    Porcentaje DECIMAL(5,2) NOT NULL  
);

Select * From Deducciones

CREATE TABLE Bonificaciones (
    ID INT PRIMARY KEY IDENTITY(1,1), 
    EmpleadoID INT NOT NULL,           
    Periodo DATE NOT NULL,              
    Monto DECIMAL(10,2) NOT NULL,      
    Descripcion NVARCHAR(100),          
    FOREIGN KEY (EmpleadoID) REFERENCES Empleados(ID) 
);

Select * From Bonificaciones

CREATE TABLE Pagos (
    ID INT PRIMARY KEY IDENTITY(1,1), 
    EmpleadoID INT NOT NULL,           
    Periodo DATE NOT NULL,             
    SalarioBruto DECIMAL(10,2) NOT NULL,
    TotalDeducciones DECIMAL(10,2) NOT NULL, 
    SalarioNeto DECIMAL(10,2) NOT NULL, 
    FOREIGN KEY (EmpleadoID) REFERENCES Empleados(ID) 
);

Select * From Pagos

